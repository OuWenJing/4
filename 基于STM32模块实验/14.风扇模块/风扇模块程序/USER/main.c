int main(void)
{
    HAL_Init();             				//初始化HAL库  
    TIM4_PWM_Init(64-1,2000-1);			//初始化定时器

	while(1)
	{
			PWM_SetTIM4Compare2(500);		//高速
			delay_ms(5000);
			PWM_SetTIM4Compare2(1200);	//中速
			delay_ms(5000);
			PWM_SetTIM4Compare2(1500);		//低速
			delay_ms(5000);
			PWM_SetTIM4Compare2(2000);	//关闭风扇
			delay_ms(5000);
	}
}

