#include "beep.h"
//==========================================================
//	函数名称：	Beep_Init()
//
//	函数功能：	初始化BEEP 控制IO
//
//	入口参数：	无
//
//	返回参数：	无
//
//	说明：		
//==========================================================
void Beep_Init(void)
{
	 P1SEL &= ~BV(6); //GPIO功能		
   P1DIR |= BV(6);  //P1.6定义为输出
}