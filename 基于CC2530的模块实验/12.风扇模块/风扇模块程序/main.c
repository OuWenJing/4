#include <ioCC2530.h>
#include "delay.h"
#include "Rs485.h"
#include "usart.h"
#include "Lamp.h"
#include "Fan.h"

//==========================================================
//	函数名称：	main()
//
//	函数功能：	程序入口函数
//
//	入口参数：	无
//
//	返回参数：	无
//
//	说明：		
//==========================================================
uint32_t Count = 0;
void main(void)
{
    Hal_Init_32M();   //初始化32M时钟
    Fan_Init();       //初始化风扇控制IO

    while(1)
    {
			Fan_Set(1);     //风扇转动
			delay_ms(5000); //延时5秒
			Fan_Set(0);     //风扇停止转运
			delay_ms(3000); //延时3秒			
    }
}
