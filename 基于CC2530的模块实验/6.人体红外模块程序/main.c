#include <ioCC2530.h>
#include "delay.h"
#include "Lamp.h"
#include "PIR.h"

//==========================================================
//	函数名称：	main()
//
//	函数功能：	程序入口函数
//
//	入口参数：	无
//
//	返回参数：	无
//
//	说明：		人体红外模块程序
//==========================================================
void main(void)
{
    Hal_Init_32M();   //初始化32M时钟
    PIR_Init();       //初始化人体红外

    while(1)
    {
        if(PIR_Read())
        {//检测到人体红外
            PIR_LED_ON();//人体红外模块上的LED2亮
        }
        else
        {//未检测到人体红外
            PIR_LED_OFF();//人体红外模块上的LED2灭
        }
    }
}
