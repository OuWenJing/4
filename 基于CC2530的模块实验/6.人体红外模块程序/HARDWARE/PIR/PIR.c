#include "delay.h"
#include "PIR.h"

//==========================================================
//	函数名称：	PIR_Init()
//
//	函数功能：	初始化人体红外
//
//	入口参数：	无
//
//	返回参数：	无
//
//	说明：		
//==========================================================
void PIR_Init(void)
{
  P0DIR |= 0x08;      //P0.3定义为输出
  
  P1SEL &= ~0x02;     //设置P1.1为普通IO口  
  P1DIR &= ~0x02;     //按键接在P1.1口上，设P1.1为输入模式 
  P1INP &= ~0x02;     //打开P1.1上拉电阻
}
