//==========================================================
//	函数名称：	EXTI_Init()
//
//	函数功能：	KEY初始化程序--外部中断方式
//
//	入口参数：	无
//
//	返回参数：	无
//
//	说明：		
//==========================================================
void EXTI_Init(void)
{
		IEN2 |= 0x10;         //端口1中断使能
		P1IEN |= 0x20;        //端口P1_5（按键S1）外部中断使能
		PICTL |= 0x20;        //端口P1_5(按键S1)下降沿触发 
		EA = 1;               //使能总中断
}